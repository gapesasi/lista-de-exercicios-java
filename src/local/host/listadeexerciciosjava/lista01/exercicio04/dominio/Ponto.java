package local.host.listadeexerciciosjava.lista01.exercicio04.dominio;

public class Ponto {
    private double pontoX;
    private double pontoY;

    public Ponto(double pontoX, double pontoY) {
        this.pontoX = pontoX;
        this.pontoY = pontoY;
    }

    public double getPontoX() {
        return pontoX;
    }

    public double getPontoY() {
        return pontoY;
    }
}
